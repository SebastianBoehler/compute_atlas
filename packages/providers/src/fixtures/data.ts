const withObservation = (
  providerId: string,
  providerSlug: string,
  marketId: string,
  region: string,
  gpuModel: string,
  priceType: 'spot' | 'on_demand' | 'auction',
  price: number,
  confidenceScore: number,
  observedAt: string,
): RawPriceObservation => ({
  id: `${providerSlug}-${marketId}-${observedAt}`,
  providerId,
  providerSlug,
  marketId,
  observedAt,
  fetchedAt: new Date().toISOString(),
  region,
  gpuModel,
  priceType,
  currency: 'USD',
  unit: 'gpu_hour',
  price,
  priceUsdPerHour: price,
  confidenceScore,
  provenanceUrl: `https://example.com/${providerSlug}/${marketId}`,
  metadata: { fixture: true },
  rawPayload: { price, gpuModel, region, priceType },
});

